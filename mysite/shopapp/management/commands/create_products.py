from django.core.management.base import BaseCommand
from shopapp.models import Product

class Command(BaseCommand):
    help = "Создание тестовых продуктов"

    def handle(self, *args, **kwargs):
        products = [
            ("Ноутбук", "Игровой ноутбук", 120000, 5),
            ("Смартфон", "Флагманский смартфон", 80000, 10),
            ("Наушники", "Беспроводные наушники", 15000, 15),
            ("Телевизор", "4K Smart TV", 65000, 7),
            ("Планшет", "Современный планшет", 45000, 8),
            ("Монитор", "Игровой монитор 144Hz", 35000, 6),
            ("Клавиатура", "Механическая клавиатура", 7000, 12),
            ("Мышь", "Игровая беспроводная мышь", 6000, 20),
            ("Видеокарта", "Графический процессор RTX", 150000, 4),
            ("Процессор", "Производительный процессор", 85000, 9),
            ("Материнская плата", "Топовая плата для сборки ПК", 30000, 10),
            ("Оперативная память", "32GB DDR5", 20000, 14),
            ("SSD", "Твердотельный накопитель 2TB", 25000, 11),
            ("HDD", "Жесткий диск 4TB", 12000, 15),
            ("Принтер", "Лазерный принтер", 18000, 7),
            ("Флешка", "USB 3.1 флеш-накопитель 128GB", 3000, 25),
            ("Часы", "Умные часы с пульсометром", 20000, 13),
            ("Колонки", "Портативная колонка Bluetooth", 9000, 18),
            ("Микрофон", "Студийный микрофон", 12000, 9),
            ("Камера", "Профессиональная камера 4K", 95000, 5)
        ]

        for name, description, price, quantity in products:
            product, created = Product.objects.get_or_create(
                name=name, defaults={"description": description, "price": price, "quantity": quantity}
            )
            status = "Создан" if created else "Уже существует"
            self.stdout.write(self.style.SUCCESS(f"{status}: {product}"))
